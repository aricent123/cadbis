<a name=raiting><center><u>Что такое рейтинг и как он растёт?</u></center>
Рейтинг- это ваш ранг на форуме. Он вычисляется в зависимости от вашей активности. Чем больше вы пишете сообщений, тем больше ваш рейтинг. Он отображается
рядом с каждым вашим сообщением в виде звёздочек. Главное- проявлять активность. Потому что если вы долгое время ничего писать не будете, ваш рейтинг постепенно упадёт.
