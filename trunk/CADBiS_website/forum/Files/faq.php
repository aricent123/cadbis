<a name=question01><center><u>Настройка сообщения.</u></center>
Сообщение можно настраивать по вашему вкусу. "Жирно" означает, что выделенный текст будет более жирным, чем остальной, курсив-текст будет под наклоном, подчеркнуть- подчёркивает выделенный текст, центрирование-центрует...:)<Br>
Так же можно вставлять цитаты и картинки. Чтобы вставить цитату, сначала скопируйте тот текст, который хотите цитировать, затем выделите его в текстовом поле и нажмите "цитировать". Чтобы цитировать определённого человека, 
проделайте те же действия, только вместо кнопки "цитировать" нажмите "цит. чел.", затем в предлагаемом окне введите ник того, кого хотите цитировать и нажмите ОК.<br>
Чтобы вставить картинку, сначала скопируйте её адрес в поле для ввода, а затем выделите и нажмите "картинка".
В сообщении так же можно использовать смайлы, кликните на картинку и код смайла добавится!
