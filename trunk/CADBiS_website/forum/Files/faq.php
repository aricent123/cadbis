<a name=register><center><u>Что даёт регистрация?</u></center>
Регистрация позволяет вам быть полноправным членом форума, писать свои сообщения, создавать новые темы, добавлять голосования и т.п.
Так же вы сможете пользоваться приватом форума.<br>
Так же, если вы зарегистрируетесь, вы сможете загрузить свою аватару, отредактировать свой профиль, вы получите ранг, который будет расти
в зависимости от количества ваших сообщений. Так же для вас будет идти учёт рейтинга, основанный на количестве написанных вами сообщений и
созданных тем.
