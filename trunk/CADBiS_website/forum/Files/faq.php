<a name=howregister><center><u>Как зарегистрироваться?</u></center>
Вы указываете всего лишь ту информацию, которую сочтёте необходимым указать. Для регистрации нужно обязательно указать
"логин"(ваш ник, имя, что хотите, лишь бы без разных специальных символов, вроде @# и т.п.) и "пароль"(любое слово, набор букв, символов, лишь бы вы его запомнили и могли бы потом запросто вспомнить для входа на форум). 
Указывайте только их, никто не потребует указать больше. Единственное, что нужно ещё выбрать- это ваш пол. По-умолчанию он установлен 
в "мужской". Будьте внимательны! Изменить пол потом нельзя! <br>
Чтобы зарегистрироваться на форуме, укажите всю желаемую информацию (всё кроме логина и пола можно впоследствии изменить), и нажмите
кнопку "региться". Если вы укажете свой e-mail, то инфа, которую вы укажете будет выслана вам на ящик, если нет, то она будет показана вам, чтобы
вы смогли её сохранить.
