<a name=whatwords><center><u>Что означают слова "последнее сообщение" "просмотров" "ответов"?</u></center>
Поле "последнее сообшение" показывает кто написал самое последнее сообщение в ту или иную тему. Вы можете легко посмотреть это
сообщение, кликнув на маленькую картинку <img src="<?php echo($file_last); ?>">. Вы будете перемещены в самый конец темы.<Br>
Поле "просмотов" показывает сколько раз была запрошена тема. <br>
Поле "ответов" показывает просто сколько ответов добавлено по данной теме.
